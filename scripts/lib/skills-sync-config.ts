/**
 * Contract data for the skills-sync checker (`scripts/check-skills-sync.ts`).
 * One place to update when the sync contract between the two managed skill
 * trees (`.claude/skills` â†” `.agents/skills`) changes: after a `10x get`
 * moves an adaptation site, after a new local extension is added, or when a
 * new per-tool adaptation is introduced. Deliberately plain data â€” easy to
 * swap wholesale once m5l4 ("Shared AI Registry") delivers stronger
 * sentinel-marker / manifest-tracked patterns.
 */

export interface AdaptedLinePair {
  /** Exact full line content in the `.claude` copy. */
  claude: string;
  /** Exact full line content in the `.agents` copy. */
  agents: string;
}

export interface SentinelFile {
  /** Path relative to each skills tree (posix separators). */
  path: string;
  /** Exact substrings that must be present in the file in BOTH trees. */
  sentinels: string[];
}

export interface SkillsSyncConfig {
  /** Repo-relative path of the Claude Code skills tree (the `10x get` target). */
  claudeSkillsDir: string;
  /** Repo-relative path of the Codex-adapted skills tree. */
  agentsSkillsDir: string;
  /** Repo-relative path of the 10x-cli manifest (the sha256 baseline of the last `10x get`). */
  manifestPath: string;
  /** Repo-relative path of the manifest-tracked prompts directory (`.claude` only â€” no `.agents` twin). */
  claudePromptsDir: string;
  /** Skills present in the trees that come from the starter bootstrap (`skills-lock.json`), not the manifest. */
  lockBootstrapSkills: string[];
  /**
   * Manifest-tracked files with INVERTED hash semantics: they carry local
   * extensions, so a manifest MISMATCH is the healthy state and a MATCH means
   * a `10x get` wiped the extension (the `755065a` incident).
   */
  extendedSkills: SentinelFile[];
  /** Hand-maintained files inside managed skill dirs: tree parity + sentinels required, no manifest baseline. */
  manualParityFiles: SentinelFile[];
  /**
   * Per-file allowlist of deliberate per-tool adaptations. Keyed by
   * tree-relative path; every entry is an exact 1:1 line substitution
   * (`.claude` line â†” `.agents` line). Any other pair difference â€” or an
   * expected substitution that is no longer present â€” is drift.
   */
  adaptedLines: Record<string, AdaptedLinePair[]>;
  /**
   * Known local deviations from the manifest baseline, pinned by sha256 of
   * the CURRENT on-disk bytes. Keyed by repo-relative path. A normal managed
   * file is OK when its hash matches the manifest OR its pinned value here;
   * anything else is drift. Extended skills ignore this map (their healthy
   * state is any-mismatch + sentinels).
   */
  acceptedLocalHashes: Record<string, string>;
}

